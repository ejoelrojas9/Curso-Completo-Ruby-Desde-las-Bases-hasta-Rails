videojuegos = ["Zelda", "Mario"]

# Para agregar elementos

# Una opción
videojuegos << "Super Smash"
["Zelda", "Mario", "Super Smash"]

# Otra opción
videojuegos.push("Mario Cart")
["Zelda", "Mario", "Super Smash", "Mario Cart"]

# Para agregar un elemento de primero
videojuegos.unshift("Bomber Man")
["Bober Man", "Zelda", "Mario", "Super Smash", "Mario Cart"]

# Añadir dos o mas registros
videojuegos += ["Call of Duty", "Halo"]

