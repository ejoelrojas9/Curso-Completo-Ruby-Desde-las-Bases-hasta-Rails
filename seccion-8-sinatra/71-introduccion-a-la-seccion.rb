Que es sinatra?

- Es un framework para desarrollar aplicaciones web utilizando el lenguaje deprogramación Rubinary
- Fue diseñado por Blake Mizerany en Calofirnia
- Es mucho más sencillo de aprender que Ruby on Rails. Por lo tanto es
excelente para que los principiantes se introduzcan al mundo del desarrollo
web
- Sitio oficial http://www.sinatradb.com/

